    private void btnCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearActionPerformed

        if (banderaboton) {
            Escritorio.add(vendedorCrear);
            vendedorCrear.show();
            banderaboton = false;
            vendedorCrear.lblTitulo.setText("Nuevo Vendedor");
            vendedorCrear.jbtnGuardar_Actualizar.setText("Guardar");
        }

    }//GEN-LAST:event_btnCrearActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (Tabla.getSelectedRow() < 0) {
            JOptionPane.showMessageDialog(null, " debemos sleccionar una fila");
        } else {
            if (banderaboton) {
                Escritorio.add(vendedorCrear);
                vendedorCrear.show();
                actualizar(Tabla.getSelectedRow());
                banderaboton = false;
                banderaGuardarActualizar = false;
                vendedorCrear.lblTitulo.setText("Actualizar Vendedor");
                vendedorCrear.jbtnGuardar_Actualizar.setText("Actualizar");
                nu = Tabla.getSelectedRow();
            }

        }
// TODO add your handling code here:
    }//GEN-LAST:event_jButton3ActionPerformed

