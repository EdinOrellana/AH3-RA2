    Modificar_GuardarVentas vendedorCrear = new Modificar_GuardarVentas();

    void actualizarTabla() {
        Tabla.setModel(controlador.TablaVentas());
    }

    public void actualizar(int id) {
        vendedorCrear.jtflCaja.setText(Modelo.datos.get(id).getCaja() + "");
        vendedorCrear.jtflGenero.setText(Modelo.datos.get(id).getGenero() + "");
        vendedorCrear.jtflNombre.setText(Modelo.datos.get(id).getNombre() + "");
        vendedorCrear.jtflVentas.setText(Modelo.datos.get(id).getVentas() + "");
        vendedorCrear.jtflcodigo.setText(Modelo.datos.get(id).getCodigo() + "");
        vendedorCrear.jPassword.setText(Modelo.datos.get(id).getPassword() + "");
    }
